#include <stdio.h>

int main(int argc, char const *argv[])
{
    // Sum
    int mySum = 10 + 10 + 1;
    printf("Sum is: %i\n", mySum);

    // Sub
    int mySub = 10 - 5;
    printf("Sub is: %d\n", mySub);

    // Multi
    int myMulti = 5 * 5;
    printf("Multi is: %i\n", myMulti);

    // Div
    int myDiv = 7 / 7;
    printf("Div is: %i\n", myDiv);

    return 0;
}

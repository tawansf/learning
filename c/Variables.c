#include <stdio.h>

int main() {
	int age, year;

	age = 26;
	year = 2025;

	int intVar = 1;
	double doubleVar = 2.3444;
	char charVar = 'T';
	float floatVar = 2.333;

	// print int
	printf("Integer => %d\n", intVar);

	// print float
	printf("Decimal => %f\n", floatVar);

	// print char
	printf("Char => %c\n", charVar);

	return 0;
}
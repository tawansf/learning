#include <iostream>
#include <cmath>
using namespace std;

int main(int argc, char const *argv[])
{
    cout << "Max: " << max(5, 10) << "\n";
    cout << "Min: " << min(5, 10) << "\n";
    cout << "Square Root: " << sqrt(25) << "\n";
    cout << "Round a number: " << round(1.85) << "\n";
    cout << "Log: " << log(10) << "\n";

    return 0;
}